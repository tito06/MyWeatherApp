package model;

public class Temperature {

    private double temp;
    private float minTemp;
    private float mazTemp;

    public double getTemp() {
        return temp;
    }

    public void setTemp(double temp) {
        this.temp = temp;
    }

    public float getMinTemp() {
        return minTemp;
    }

    public void setMinTemp(float minTemp) {
        this.minTemp = minTemp;
    }

    public float getMazTemp() {
        return mazTemp;
    }

    public void setMazTemp(float mazTemp) {
        this.mazTemp = mazTemp;
    }
}
